export class Footprint {

  //Questa classe rappresenta semplicemente un'orma.
  //E' costituita da delle coordinate e dal colore dell'orma
  posX: number;
  posY: number;
  color: string;

  constructor(posX, posY, color) {
    this.posX = posX;
    this.posY = posY;
    this.color = color;
  }

}
